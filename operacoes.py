# Definindo a forma de resolução de cada operação

def soma(n1, n2):
    return n1 + n2


def subtracao(n1, n2):
    return n1 - n2


def multiplicacao(n1, n2):
    return n1 * n2


def divisao(n1, n2):
    return n1 / n2


def exponencial(n1, n2):
    return n1 ** n2


def raiz(n1, n2):
    return n1 ** (1/n2)
